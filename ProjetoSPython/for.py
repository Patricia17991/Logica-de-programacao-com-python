#FOR - o "PARA" da programação
#Imagine que vc é um professor fazendo a chamada.

alunos = ["Ana","João","Marcia","Lina"]

for aluno in alunos: #para CADA aluno na lista
   print(f"{aluno} está presente!")
   
print("Chamada concluída")
#PARA CADA aluno na lista, diga que ele está presente.