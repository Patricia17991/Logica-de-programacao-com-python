# WHILE - equivale ao "enquanto" da programação.
#Imagine que vc está com sede e enchendo um copo de água...

copo = 0
nivel_maximo = 10

while copo < nivel_maximo: #enquanto o copo não estiver cheio
   print("adicionando água...")
   copo += 1 #adiciona +1 de água
   print(f"copo está com {copo} unidades de água")
   
print("Copo cheio! Hora de parar de encher ele")
#Resumindo: no código ENQUANTO (while) o copo não estiver cheio ele continuará recebendo água